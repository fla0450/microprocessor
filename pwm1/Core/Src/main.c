int __io_putchar(int ch) {
   HAL_UART_Transmit(&huart2, (uint8_t *)&ch, 1, 1000);
   if (ch == '\n') {
      HAL_UART_Transmit(&huart2, (uint8_t *)"\r", 1, 1000);
   }
   return ch;
}
